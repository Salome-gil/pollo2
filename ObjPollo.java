public class ObjPollo {
    String Marca;
    String TipoCoccion;
    String Presas;
    double Precio;
    public String getMarca() {
        return Marca;
    }
    public void setMarca(String marca) {
        Marca = marca;
    }
    public String getTipoCoccion() {
        return TipoCoccion;
    }
    public void setTipoCoccion(String tipoCoccion) {
        TipoCoccion = tipoCoccion;
    }
    public String getPresas() {
        return Presas;
    }
    public void setPresas(String presas) {
        Presas = presas;
    }
    public double getPrecio() {
        return Precio;
    }
    public void setPrecio(double precio) {
        Precio = precio;
    }
    

    
}